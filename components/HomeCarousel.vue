<script>
import imagesloaded from 'imagesloaded'
import anime from 'animejs'
export default {
    name: 'HomeCarousel',
    data() {
        return {
            active: false,
        }
    },
    mounted() {
        imagesloaded(document.querySelector('body'), () => {
            this.initCarousel()
            this.triggerAnimation()
            window.addEventListener(
                'scroll',
                () => {
                    this.triggerAnimation()
                },
                false
            )
        })
    },
    methods: {
        triggerAnimation() {
            const carousel = document.getElementById('homecarouselbox')
            if (window.scrollY >= carousel.offsetTop * 0.98 && !this.active) {
                this.active = true
                anime
                    .timeline({ loop: false })
                    .add({
                        targets: '.carousel\\.title',
                        opacity: [0, 1],
                        translateY: ['-45px', 0],
                        duration: 600,
                        easing: 'easeInOutExpo',
                        elasticity: 700,
                    })
                    .add({
                        targets: '#homecarousel',
                        opacity: [0, 1],
                        translateY: ['-45px', 0],
                        duration: 600,
                        easing: 'easeInOutExpo',
                        elasticity: 700,
                    })
                    .add({
                        targets: '.homecarosuelfooter',
                        opacity: [0, 1],
                        translateY: ['45px', 0],
                        duration: 600,
                        easing: 'easeInOutExpo',
                        elasticity: 700,
                    })
            }
        },
        initCarousel() {
            window.$('#homecarousel').slick({
                arrows: true,
                nextArrow: window.$('#next'),
                prevArrow: window.$('#prev'),
                slidesToScroll: 1,
                slidesToShow: 1,
                swipe: false,
            })
        },
    },
}
</script>
